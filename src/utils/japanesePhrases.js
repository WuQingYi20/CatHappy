// 获取随机每日激励
export const getRandomDailyMotivation = () => {
  return dailyMotivation[Math.floor(Math.random() * dailyMotivation.length)];
};

// 获取随机猫咪短语
export const getRandomCatPhrase = (mood = null) => {
  const phrases = mood
    ? catPhrases.filter(p => p.mood === mood)
    : catPhrases;
  return phrases[Math.floor(Math.random() * phrases.length)];
};

// 获取随机会话短语
export const getRandomConversationPhrase = () => {
  return conversationPhrases[Math.floor(Math.random() * conversationPhrases.length)];
};

// 获取随机学习短语
export const getRandomStudyPhrase = () => {
  return studyPhrases[Math.floor(Math.random() * studyPhrases.length)];
};

// 检查成就解锁
export const checkAchievement = (pomodoros, streak) => {
  return achievements.filter(achievement => {
    if (achievement.unlockAt && pomodoros === achievement.unlockAt) {
      return true;
    }
    if (achievement.streak && streak === achievement.streak) {
      return true;
    }
    return false;
  });
};
